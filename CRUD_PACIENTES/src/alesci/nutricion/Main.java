    //Funcion para registrar una nueva pelicula
    private void Registrar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Registrar
        //Sacamos los valores de los jtextfield y los jcombos
        String nombre = txNombre1.getText();
        int edad = Integer.parseInt(txEdad1.getText());
        String telefono =txTelefono1.getText();
        String direccion = txDireccion1.getText();
        int peso = Integer.parseInt(txPeso1.getText());
        int talla = Integer.parseInt(txTalla1.getText());
        String ubicacion = String.valueOf(txUbicacion1.getSelectedItem());
        String condicionBase = txCondicionBase1.getText();
        String medicamentos = txMedicamentos1.getText();
     
        int cribaje=0;
        float evaluacion=0;
        float evaluacionGlobal=0;
        if(crud.insert(nombre,edad,telefono,direccion,peso,talla,ubicacion,condicionBase,medicamentos,cribaje,evaluacion,evaluacionGlobal)){
            JOptionPane.showMessageDialog(this, "Paciente registrado con exito.", "Exito", JOptionPane.INFORMATION_MESSAGE);
            txTelefono1.setText("");
            txEdad1.setText("");
            cbPacientes.removeAllItems();
        cbPacientes1.removeAllItems();
        cbPacientes4.removeAllItems();
        cbPacientes5.removeAllItems();
        cbPacientes8.removeAllItems();
            cargarPacientes();
        }else{
            JOptionPane.showMessageDialog(this, "Error al registrar los datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }   
    }//GEN-LAST:event_Registrar

