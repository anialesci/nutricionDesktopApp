    //Funcion que carga todas la peliculas en la tabla y en los JCOMboBox
    private void cargarPacientes(){
        limpiarTabla();
        try{
            ResultSet rs = crud.selectXtodas();
            while(rs.next()){
                Object row [] = new Object[13];
                for (int i = 0; i < 13; i++) {
                    row[i] = rs.getObject(i+1); //El resulset los indices empiezan en 1, mi for lo empieso en 0 por eso le sumo i+1
                }
                //Agregamos cada registro al modelo de la tabla
                modelo.addRow(row);
                cbPacientes.addItem(new Paciente(Integer.parseInt(row[0].toString()),row[1].toString(),Integer.parseInt(row[2].toString()),
                        row[3].toString(),row[4].toString(),
                        Integer.parseInt(row[5].toString()),Integer.parseInt(row[6].toString()),
                row[7].toString(),row[8].toString(),row[9].toString(),Integer.parseInt(row[10].toString()),Float.parseFloat(row[11].toString()),Float.parseFloat(row[12].toString())));
            cbPacientes1.addItem(new Paciente(Integer.parseInt(row[0].toString()),row[1].toString(),Integer.parseInt(row[2].toString()),
                        row[3].toString(),row[4].toString(),
                        Integer.parseInt(row[5].toString()),Integer.parseInt(row[6].toString()),
                row[7].toString(),row[8].toString(),row[9].toString(),Integer.parseInt(row[10].toString()),Float.parseFloat(row[11].toString()),Float.parseFloat(row[12].toString())));
            
           cbPacientes4.addItem(new Paciente(Integer.parseInt(row[0].toString()),row[1].toString(),Integer.parseInt(row[2].toString()),
                        row[3].toString(),row[4].toString(),
                        Integer.parseInt(row[5].toString()),Integer.parseInt(row[6].toString()),
                row[7].toString(),row[8].toString(),row[9].toString(),Integer.parseInt(row[10].toString()),Float.parseFloat(row[11].toString()),Float.parseFloat(row[12].toString())));
            
          cbPacientes5.addItem(new Paciente(Integer.parseInt(row[0].toString()),row[1].toString(),Integer.parseInt(row[2].toString()),
                        row[3].toString(),row[4].toString(),
                        Integer.parseInt(row[5].toString()),Integer.parseInt(row[6].toString()),
                row[7].toString(),row[8].toString(),row[9].toString(),Integer.parseInt(row[10].toString()),Float.parseFloat(row[11].toString()),Float.parseFloat(row[12].toString())));
            
          cbPacientes8.addItem(new Paciente(Integer.parseInt(row[0].toString()),row[1].toString(),Integer.parseInt(row[2].toString()),
                        row[3].toString(),row[4].toString(),
                        Integer.parseInt(row[5].toString()),Integer.parseInt(row[6].toString()),
                row[7].toString(),row[8].toString(),row[9].toString(),Integer.parseInt(row[10].toString()),Float.parseFloat(row[11].toString()),Float.parseFloat(row[12].toString())));
            
            }
        }catch(Exception e){
            e.printStackTrace();
             JOptionPane.showMessageDialog(this, "Error al cargar los datos de la Base de Datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    //Funcion para limpiar la tabla
    private void limpiarTabla(){
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i-=1;
        }
    }
    
    //Clase con los atributos de la clase pelicula de la Base de datos
    class Paciente
    {
        private int id,edad,peso, talla, cribaje;
        private float evaluacion, evaluacionGlobal;
        private String nombre,telefono, direccion,ubicacion, condicionBase, medicamentos;
        
 
        public Paciente(int id, String nombre,
                int edad, String telefono,String direccion, int peso, int talla,
                String ubicacion, String condicionBase, String medicamentos, int cribaje, float evaluacion, float evaluacionGlobal)
        {
            this.id = id;
            this.nombre = nombre;
            this.edad = edad;
            this.telefono = telefono;
            this.direccion = direccion;
            this.peso = peso;
            this.talla = talla;
            this.ubicacion = ubicacion;
            this.condicionBase = condicionBase;
            this.medicamentos = medicamentos;
            this.cribaje = cribaje;
            this.evaluacion = evaluacion;
            this.evaluacionGlobal = evaluacionGlobal;
        }
 
        public int getId()
        {
            return id;
        }
 
        public String getNombre()
        {
            return nombre;
        }

         public void setNombre(String nombre) {
            this.nombre = nombre;
        }
         
        public int getEdad() {
            return edad;
        }

        public void setEdad(int edad) {
            this.edad = edad;
        }

        public String getTelefono() {
            return telefono;
        }

        public void setTelefono(String telefono) {
            this.telefono = telefono;
        }

        public int getPeso() {
            return peso;
        }

        public int getCribaje() {
            return cribaje;
        }

        public float getEvaluacionGlobal() {
            return evaluacionGlobal;
        }

        public void setEvaluacionGlobal(float evaluacionGlobal) {
            this.evaluacionGlobal = evaluacionGlobal;
        }

        public String getUbicacion() {
            return ubicacion;
        }

        public void setUbicacion(String ubicacion) {
            this.ubicacion = ubicacion;
        }

        public void setCribaje(int cribaje) {
            this.cribaje = cribaje;
        }

        public float getEvaluacion() {
            return evaluacion;
        }

        public void setEvaluacion(float evaluacion) {
            this.evaluacion = evaluacion;
        }

        public void setPeso(int peso) {
            this.peso = peso;
        }

        public int getTalla() {
            return talla;
        }

        public void setTalla(int talla) {
            this.talla = talla;
        }


        public String getDireccion() {
            return direccion;
        }

        public void setDireccion(String direccion) {
            this.direccion = direccion;
        }

        public String getCondicionBase() {
            return condicionBase;
        }

        public void setCondicionBase(String condicionBase) {
            this.condicionBase = condicionBase;
        }

        public String getMedicamentos() {
            return medicamentos;
        }

        public void setMedicamentos(String medicamentos) {
            this.medicamentos = medicamentos;
        }

       
        @Override
        public String toString()
        {
            return nombre;
        }
    }
    
